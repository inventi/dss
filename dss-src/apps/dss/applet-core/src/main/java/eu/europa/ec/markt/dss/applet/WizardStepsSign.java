    private void setColorOnPanelList(final Color color, final Component... comps) {
        for (Component p : comps) {
            p.setBackground(color);
            p.repaint();
        }
    }

    private int currentStep = 0;

    /*
     * (non-Javadoc)
     * 
     * @see javax.swing.JComponent#paint(java.awt.Graphics)
     */
    @Override
    public void paint(Graphics g) {
        Color blue = new Color(0x99, 0xCC, 0xFF);
        switch (this.currentStep) {
        case 1:
            setColorOnPanelList(blue, jPanel1);
            break;
        case 2:
            setColorOnPanelList(blue, jPanel2);
            break;
        case 3:
            setColorOnPanelList(blue, jPanel3);
            break;
        case 4:
            setColorOnPanelList(blue, jPanel4);
            break;
        case 5:
            setColorOnPanelList(blue, jPanel5);
            break;
        case 6:
            setColorOnPanelList(blue, jPanel6);
            break;
        case 7:
            setColorOnPanelList(blue, jPanel7);
            break;
        }
        super.paint(g);
    }

    /*
     * (non-Javadoc)
     * 
     * @see eu.europa.ec.markt.dss.applet.WizardSteps#setCurrentStep(int)
     */
    @Override
    public void setCurrentStep(int step) {
        this.currentStep = step;
        setColorOnPanelList(new Color(240, 240, 240), jPanel1, jPanel2, jPanel3, jPanel4, jPanel5, jPanel6, jPanel7);
        repaint();
        if (getParent() != null) {
            getParent().repaint();
        }
    }

