    @Override
    public void aboutToDisplayPanel() {

        String originalName = null; 
        File parentDir = null;
        
        switch (model.getWizardUsage()) {
        case SIGN:
            getWizard().setStepsProgression(7);
            originalName = model.getOriginalFile().getName();
            parentDir = model.getOriginalFile().getParentFile();
            break;
        case EXTEND:
            getWizard().setStepsProgression(3);
            originalName = model.getSignedFile().getName();
            parentDir = model.getSignedFile().getParentFile();
            break;
        }

        String originalExtension = null;
        int i2 = originalName.lastIndexOf(".");
        if (i2 > 0) {
            originalExtension = originalName.substring(i2);
            originalName = originalName.substring(0, i2);
        }

        String newName = null;
        if (model.getWizardUsage() == WizardUsage.EXTEND) {
            newName = originalName + "-extended" + originalExtension;
        } else if (model.getPackaging() == SignaturePackaging.ENVELOPING
                && model.getSignatureFormat().startsWith("XAdES")) {
            newName = originalName + "-signed" + ".xml";
        } else if (model.getPackaging() == SignaturePackaging.DETACHED
                && model.getSignatureFormat().startsWith("XAdES")) {
            newName = originalName + "-signed" + ".xml";
        } else if (model.getSignatureFormat().startsWith("CAdES-")
                && !model.getOriginalFile().getName().endsWith(".p7m")) {
            newName = model.getOriginalFile().getName() + ".p7m";
        } else if (model.getSignatureFormat().startsWith("ASiC-")) {
            newName = model.getOriginalFile().getName() + ".asics";
        } else {
            int i = model.getOriginalFile().getName().lastIndexOf(".");
            if (i > 0) {
                newName = model.getOriginalFile().getName().substring(0, i) + "-signed"
                        + model.getOriginalFile().getName().substring(i);
            } else {
                newName = model.getOriginalFile().getName() + "-signed";
            }
        }

        choosedFile = new File(parentDir.getParentFile(), newName);

        jFileChooser1.setCurrentDirectory(choosedFile.getParentFile());
        jFileChooser1.setSelectedFile(choosedFile);

        filename.setText(choosedFile.getAbsolutePath());

    }

    @Override
    public void aboutToHidePanel() {
        switch(model.getWizardUsage()) {
        case SIGN:
            model.setSignedFile(jFileChooser1.getSelectedFile());
            break;
        case EXTEND: 
            model.setExtendedFile(jFileChooser1.getSelectedFile());
            break;
        }
    }

